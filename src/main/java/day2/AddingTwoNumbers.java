package day2;

public class AddingTwoNumbers {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        int sum = x+y;
        System.out.println(x+" + "+y+"="+sum);
        System.out.println(x+" - "+y+"="+(x-y));
        System.out.println(x+" * "+y+"="+(x*y));
        System.out.println(x+" / "+y+"="+(x/y));
        System.out.println(y+" % "+x+"="+(y%x));
    }
}
